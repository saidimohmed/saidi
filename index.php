        <script>
        const prizes = {
            0: $("#img0").attr('src'),
            1: $("#img1").attr('src'),
            2: $("#img2").attr('src'),
            3: $("#img3").attr('src'),
            4: $("#img4").attr('src'),
            5: $("#img5").attr('src'),
            6: $("#img6").attr('src'),
            7: $("#img7").attr('src'),
        };

        const total_items = 8;
        const minimum_jumps = 5; 
        let keys = -1;
        let jumps = 0;
        let speed = 10;
        let timer = 0;
        let prize = -1;

        function runCircle() {
            soundEffect('spinSound');
            $(`[data-order="${keys}"]`).removeClass('active');

            keys += 1;

            if (keys > total_items - 1) {
                keys = 0;
            }
            
            $(`[data-order="${keys}"]`).addClass('active');
        }

        function generatePrizeNumber() {
            return Math.floor(Math.random() * total_items);
        }

        function controllSpeed() {
            jumps += 1;
            runCircle();
            if (jumps > minimum_jumps + 10 && prize === keys) {
                clearTimeout(timer);
                console.log(keys)
                setTimeout(() => {
                var src = prizes[keys]
                $('.loading').fadeIn().css('display','flex'),$('.mask').fadeIn()
                $('.spinButton').css('pointer-events','unset');
                $('.sub').css('pointer-events','unset');
                setTimeout(() => {
                    $('.loading').fadeOut()
                    $('.popUp').fadeIn().css('display','flex'),$('#imgSource').attr('src',src),$('.mask').fadeIn()
                    soundEffect('pop')
                },1000)
                },1000)

                prize = -1;
                jumps = 0;

            } else {
                if (jumps < minimum_jumps) {
                    speed -= 5; 
                } else if (jumps === minimum_jumps) {
                    const random_number = generatePrizeNumber();
                    prize = random_number;
                } else {
                
                    if ( (jumps > minimum_jumps + 10) && prize === (keys + 1) ) {
                        speed += 600;
                    } else {
                        speed += 20; 
                    }
                }
                if (speed < 40) {
                    speed = 40;
                }

                timer = setTimeout(controllSpeed, speed);
            }
            }

            function init(e) {
                e.style.pointerEvents = 'none';
                $('.sub').css('pointer-events','none');
                jumps = 0;
                speed = 100;
                prize = -1;
                controllSpeed();
            }
            function soundEffect(e) {
                var audio;
                if(e == 'spinSound')
                {
                    audio = new Audio('https://j.top4top.io/m_1948rgfbd0.mp3');
                }
                if(e == 'click')
                {
                    audio = new Audio('https://l.top4top.io/m_2093up9a11.mp3');
                    
                }
                if(e == 'pop')
                {
                    audio = new Audio('https://f.top4top.io/m_2093okcys1.mp3');
                }
                audio.play();
            }
